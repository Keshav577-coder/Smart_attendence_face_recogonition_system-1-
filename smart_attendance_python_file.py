for filename in uploaded_files:
    print(f"\n--- Analyzing {filename} ---")

    # Load image
    test_img = cv2.imread(filename)
    if test_img is None:
        print(f"Skipping {filename}: Could not read file.")
        continue

    imgRGB = cv2.cvtColor(test_img, cv2.COLOR_BGR2RGB)

    # Find faces and encodings in this specific image
    facesCurFrame = face_recognition.face_locations(imgRGB)
    encodesCurFrame = face_recognition.face_encodings(imgRGB, facesCurFrame)

    if not encodesCurFrame:
        print(f"No faces detected in {filename}")
        continue

    for encodeFace, faceLoc in zip(encodesCurFrame, facesCurFrame):
        # Compare with your database
        matches = face_recognition.compare_faces(encodeListKnown, encodeFace)
        faceDis = face_recognition.face_distance(encodeListKnown, encodeFace)
        matchIndex = np.argmin(faceDis)

        if matches[matchIndex]:
            name = classNames[matchIndex].upper()
            print(f"✅ Match Found: {name}")
            markAttendance(name) # Logs to CSV

            # Draw visual feedback
            y1, x2, y2, x1 = faceLoc
            cv2.rectangle(test_img, (x1, y1), (x2, y2), (0, 255, 0), 2)
            cv2.putText(test_img, name, (x1, y1-10), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 255, 0), 2)
        else:
            print(f"❌ Unknown Face in {filename}")

    # Show the result for each image
    cv2_imshow(test_img)

