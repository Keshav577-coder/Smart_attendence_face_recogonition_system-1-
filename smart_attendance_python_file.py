        myDataList = f.readlines()
        nameList = [line.split(',')[0] for line in myDataList]

        if name not in nameList:
            now = datetime.now()
            dtString = now.strftime('%H:%M:%S')
            dateString = now.strftime('%d-%B-%Y')
            f.writelines(f'\n{name},{dtString},{dateString}')
            print(f"Logged: {name}")

