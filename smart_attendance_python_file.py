print(f"Checking path: {train_path}")

if not os.path.exists(train_path):
    print("Error: Path still not found. Double check the folder names.")
else:
    all_files = os.listdir(train_path)
    print(f"Found {len(all_files)} total files.")

    for img_name in all_files:
        if not img_name.lower().endswith(('.png', '.jpg', '.jpeg')):
            continue

        # 2. Extract name from filename (e.g., 'Abir.6.3051.10.jpg' -> 'Abir')
        student_name = img_name.split('.')[0]

        # To keep it fast, let's only take 3 images per student
        if classNames.count(student_name) >= 3:
            continue

        img_path = os.path.join(train_path, img_name)
        curImg = cv2.imread(img_path)

        if curImg is not None:
            # Convert BGR to RGB
            imgRGB = cv2.cvtColor(curImg, cv2.COLOR_BGR2RGB)

            # Encode
            encodes = face_recognition.face_encodings(imgRGB)
            if len(encodes) > 0:
                encodeListKnown.append(encodes[0])
                classNames.append(student_name)
                print(f"Encoded: {student_name} ({img_name})")

    print(f"\n✅ Encoding Complete!")
    print(f"Total Encodings: {len(encodeListKnown)}")
    print(f"Unique Students: {len(set(classNames))}")

